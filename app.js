
$("#show").click(function() {
  $(".hidden").fadeOut("slow");

  $(this).addClass("selected");
  $(this).removeClass("notSelected");

  $("#hide").addClass("notSelected");
  $("#hide").removeClass("selected");
});

$("#hide").click(function() {
  $(".hidden").fadeIn("slow");

  $(this).addClass("selected");
  $(this).removeClass("notSelected");

  $("#show").addClass("notSelected");
  $("#show").removeClass("selected");
});

